package com.sl.fdc.dto;

/**
 *
 * @author Stefan Lourens
 */
public class Team {

	private int id;
	private String name;
	private String shortName;
	private String crestUrl;

	public Team() {
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getShortName() {
		return shortName;
	}

	public String getCrestUrl() {
		return crestUrl;
	}

}
